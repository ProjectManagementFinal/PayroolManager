
public class Benefits extends Employee{

	private float elderlyBonus;
	private float healthCare;
	private float loyaltyBonus;
	private float employeeOfMonth;
	
	public float getElderlyBonus() {
		return elderlyBonus;
	}
	public void setElderlyBonus(float elderlyBonus) {
		this.elderlyBonus = elderlyBonus;
	}
	public float getHealthCare() {
		return healthCare;
	}
	public void setHealthCare(float healthCare) {
		this.healthCare = healthCare;
	}
	public float getLoyaltyBonus() {
		return loyaltyBonus;
	}
	public void setLoyaltyBonus(float loyaltyBonus) {
		this.loyaltyBonus = loyaltyBonus;
	}
	public float getEmployeeOfMonth() {
		return employeeOfMonth;
	}
	public void setEmployeeOfMonth(float employeeOfMonth) {
		this.employeeOfMonth = employeeOfMonth;
	}
	
	
}
